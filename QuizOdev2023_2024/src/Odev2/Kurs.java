package Odev2;

public class Kurs {
	private int kursId;
	private String kursAd;
	
	public Kurs(int kursId, String kursAd) {
		super();
		this.kursId = kursId;
		this.kursAd = kursAd;
	}

	public int getKursId() {
		return kursId;
	}

	public String getKursAd() {
		return kursAd;
	}
	
}
